ALTER TABLE public.support_tickets ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.support_engineering_tasks ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.support_ticket_events ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.support_notifications ENABLE ROW LEVEL SECURITY;

-- Tickets: users can create and read only their own support reports.
CREATE POLICY "Users can view own support tickets"
  ON public.support_tickets FOR SELECT
  USING (auth.uid() = user_id);

CREATE POLICY "Users can insert own support tickets"
  ON public.support_tickets FOR INSERT
  WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Service role can manage support tickets"
  ON public.support_tickets FOR ALL
  USING (auth.role() = 'service_role')
  WITH CHECK (auth.role() = 'service_role');

-- Engineering tasks: users can read tasks tied to their own tickets.
CREATE POLICY "Users can view own support engineering tasks"
  ON public.support_engineering_tasks FOR SELECT
  USING (
    EXISTS (
      SELECT 1
      FROM public.support_tickets t
      WHERE t.id = support_ticket_id
        AND t.user_id = auth.uid()
    )
  );

CREATE POLICY "Users can insert support engineering tasks for own tickets"
  ON public.support_engineering_tasks FOR INSERT
  WITH CHECK (
    EXISTS (
      SELECT 1
      FROM public.support_tickets t
      WHERE t.id = support_ticket_id
        AND t.user_id = auth.uid()
    )
  );

CREATE POLICY "Service role can manage support engineering tasks"
  ON public.support_engineering_tasks FOR ALL
  USING (auth.role() = 'service_role')
  WITH CHECK (auth.role() = 'service_role');

-- Events: users can read timeline events for their own tickets.
CREATE POLICY "Users can view own support ticket events"
  ON public.support_ticket_events FOR SELECT
  USING (
    EXISTS (
      SELECT 1
      FROM public.support_tickets t
      WHERE t.id = support_ticket_id
        AND t.user_id = auth.uid()
    )
  );

CREATE POLICY "Service role can manage support ticket events"
  ON public.support_ticket_events FOR ALL
  USING (auth.role() = 'service_role')
  WITH CHECK (auth.role() = 'service_role');

-- Notifications: users can read notifications addressed to them.
CREATE POLICY "Users can view own support notifications"
  ON public.support_notifications FOR SELECT
  USING (auth.uid() = user_id);

CREATE POLICY "Service role can manage support notifications"
  ON public.support_notifications FOR ALL
  USING (auth.role() = 'service_role')
  WITH CHECK (auth.role() = 'service_role');
